    /** يحدّد مفتاح نوع القطار من الوصف العربي (يُستخدم لتصنيف القطار). */
    public static function tariffKey(string $arabicType): string
    {
        foreach (['نوم', 'تالجو', 'VIP', 'مميز', 'إسباني', 'اسباني', 'مكيف', 'روسي'] as $needle) {
            if (mb_stripos($arabicType, $needle) !== false) {
                return match ($needle) {
                    'اسباني' => 'إسباني',
                    default => $needle,
                };
            }
        }

        return 'مختلط';
    }

    /** توحيد كتابة العربية (الهمزات، التاء المربوطة، الألف المقصورة، التشكيل) للمطابقة. */
    public static function normalize(string $text): string
    {
        $text = trim($text);
        $text = str_replace(['أ', 'إ', 'آ', 'ٱ'], 'ا', $text);
        $text = str_replace('ى', 'ي', $text);
        $text = str_replace('ة', 'ه', $text);
        $text = preg_replace('/[\x{064B}-\x{0652}\x{0640}]/u', '', $text); // تشكيل وتطويل
        $text = preg_replace('/\s+/u', ' ', $text);

        return $text;
    }

    /** بحث في خريطة معطاة بمطابقة الاسم بعد التوحيد. */
    private static function lookupNormalized(array $map, string $nameAr): mixed
    {
        $target = self::normalize($nameAr);
        foreach ($map as $key => $value) {
            if (self::normalize($key) === $target) {
                return $value;
            }
        }

        return null;
    }

    /** @return array{0:float,1:float}|null إحداثيات المحطة إن وُجدت. */
    public static function coordsFor(string $nameAr): ?array
    {
        return self::lookupNormalized(self::COORDS, $nameAr);
    }

    private const OBS_BASE = 'https://obs.enr.gov.eg/o-city/obs/enr/railway/ar/booktickets';

    /**
     * رابط الحجز الرسمي. يُمرَّر رمزا المحطتين (booking_name الرسمي مثل CAIRO)
     * فتُعبَّأ مسبقًا؛ لو غير متوفرين تُفتح صفحة الحجز بدون تحديد المحطة.
     */
    public static function bookingUrl(?string $fromCode, ?string $toCode, ?string $date): string
    {
        $params = ['trip' => 'oneway'];
        if ($date) {
            $params['departure'] = $date;
        }
        if ($fromCode && $toCode) {
            $params['from'] = $fromCode;
            $params['to'] = $toCode;
        }

        return self::OBS_BASE.'?'.http_build_query($params);
    }
